
package pe.edu.uni.EduTec.service;


public class EduTecService {
    
    public double calcularPromedio(double n1,double n2)
    {
        return (n1+n2)/2;
    }
    public double calcularPromedio(double n1,double n2,double n3)
    {
        return (n1+n2+n3)/3;
    }
    public double calcularPromedio(double n1,double n2,double n3,double n4)
    {
        return (n1+n2+n3+n4)/4;
    }
    public double calcularPromedio(double n1,double n2,double n3,double n4,double n5)
    {
        return (n1+n2+n3+n4+n5)/5;
    }
}
